private void startTransition(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startTransition
    transition.setRectangleTransition(GraphicUtilities.getRectangleFromComponent(panelMain, transition));
    transition.startTransition(500);

    ((CardLayout) panelMain.getLayout()).show(panelMain, evt.getActionCommand());
}//GEN-LAST:event_startTransition

private void changeTransitionType(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeTransitionType
    transition.setTransition((Transition) comboType.getSelectedItem());
}//GEN-LAST:event_changeTransitionType

private void panelColorMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelColorMousePressed
    color = JColorChooser.showDialog(this, "Choose Color Transition", (Color) transition.getColorTransition());
    transition.setColorTransition(color);
    panelColor.setBackground(color);
}//GEN-LAST:event_panelColorMousePressed

