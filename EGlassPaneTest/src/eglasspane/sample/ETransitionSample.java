    public void actionPerformed(java.awt.event.ActionEvent evt) {
        if (evt.getSource() == buttonImage2) {
            ETransitionSample.this.startTransition(evt);
        }
        else if (evt.getSource() == buttonImage1) {
            ETransitionSample.this.startTransition(evt);
        }
        else if (evt.getSource() == comboType) {
            ETransitionSample.this.changeTransitionType(evt);
        }
        else if (evt.getSource() == buttonImage3) {
            ETransitionSample.this.startTransition(evt);
        }
    }

