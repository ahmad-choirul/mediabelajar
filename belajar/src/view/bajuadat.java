public void Musik_Play() {
        try {
            audioIn = AudioSystem.getAudioInputStream(new File("aset\\suara_propinsi\\propinsi"+(set+1)+".wav"));
            
            clip = AudioSystem.getClip();
            clip.open(audioIn);
            clip.loop(0);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
        }
    }
